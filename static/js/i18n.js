let currentLang = localStorage.getItem("paperlang") || "zh";

// 同步到 window.PaperLens 供 inline 代码使用
if (!window.PaperLens) window.PaperLens = {};
Object.defineProperty(window.PaperLens, 'currentLang', {
  get() { return currentLang; },
  set(v) { currentLang = v; },
  configurable: true,
});

function t(key) {
  return LANGS[currentLang]?.[key] || LANGS.zh[key] || key;
}

// 翻译后端返回的错误码
function te(error) {
  if (!error) return "";
  // 处理 AI_ERROR: 前缀
  if (error.startsWith("AI_ERROR:")) {
    const parts = error.split(":");
    const code = parts[1];
    const detail = parts.slice(2).join(":");
    const msg = LANGS[currentLang]?.[code] || LANGS.zh?.[code] || code;
    return detail ? `${msg}: ${detail}` : msg;
  }
  return LANGS[currentLang]?.[error] || LANGS.zh?.[error] || error;
}

// 翻译 AI 响应（处理 AI_ERROR: 前缀）
function teAI(response) {
  if (!response) return "";
  if (response.startsWith("AI_ERROR:")) {
    return te(response);
  }
  return response;
}

// 安全设置元素属性的辅助函数
function safeSetDisabled(id, disabled) {
  const el = document.getElementById(id);
  if (el) el.disabled = disabled;
}

// 语言切换
function setLang(lang) {
  currentLang = lang;
  localStorage.setItem("paperlang", lang);
  // 调用 inline 的 applyLang（通过 window 引用）
  if (typeof window.applyLang === 'function') window.applyLang();
  // 如果有搜索结果，重新渲染
  if (window.PaperLens?.allPapers?.length) {
    if (typeof window.renderResults === 'function') window.renderResults();
    if (typeof window.renderPagination === 'function') window.renderPagination();
  }
}

// 导出到 window 供 inline onclick 使用
window.setLang = setLang;

export { LANGS, currentLang, t, te, teAI, safeSetDisabled, setLang };